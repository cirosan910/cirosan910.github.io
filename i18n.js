(function(){
  var STORE='cirosan-lang';
  function apply(lang){
    if(!window.I18N[lang]) lang='en';
    var dict=window.I18N[lang];
    document.querySelectorAll('[data-i18n]').forEach(function(el){
      var k=el.getAttribute('data-i18n');
      if(dict[k]!=null) el.innerHTML=dict[k];
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(function(el){
      var k=el.getAttribute('data-i18n-ph');
      if(dict[k]!=null) el.setAttribute('placeholder',dict[k]);
    });
    document.querySelectorAll('[data-img]').forEach(function(el){
      var slot=el.getAttribute('data-img');
      el.style.backgroundImage="url('images/"+slot+"-"+lang+".jpg')";
      el.style.backgroundSize="cover";
      el.style.backgroundPosition="center";
      el.style.backgroundRepeat="no-repeat";
      el.classList.add('has-img');
    });
    document.querySelectorAll('[data-img-bg]').forEach(function(el){
      var proj=el.getAttribute('data-img-bg');
      var bg=el.querySelector('.project-img-bg');
      if(bg){
        bg.style.backgroundImage="url('images/"+proj+"/cover-"+lang+".jpg')";
        bg.style.backgroundSize="cover";
        bg.style.backgroundPosition="center";
      }
      el.classList.add('has-img');
    });
    document.documentElement.setAttribute('lang',lang);
    document.querySelectorAll('.lang-opt').forEach(function(o){
      o.classList.toggle('active', o.getAttribute('data-lang')===lang);
    });
    try{localStorage.setItem(STORE,lang);}catch(e){}
  }
  function init(){
    var lang='en';
    try{lang=localStorage.getItem(STORE)||'en';}catch(e){}
    apply(lang);
    document.querySelectorAll('.lang-opt').forEach(function(o){
      o.addEventListener('click',function(e){e.preventDefault();apply(o.getAttribute('data-lang'));});
    });
  }
  if(document.readyState!=='loading'){init();}else{document.addEventListener('DOMContentLoaded',init);}
})();
